<template>
  <label
    class="px-6 py-3 font-normal tracking-wide text-white rounded cursor-pointer hover:bg-gray-700"
    :class="{
      'bg-green-500': modelValue,
      'bg-gray-500': !modelValue,
    }"
  >
    <input
      type="checkbox"
      :value="modelValue"
      @input="() => $emit('update:modelValue', !modelValue)"
      class="hidden"
    />
    {{ modelValue ? 'Completed!' : 'Mark as complete' }}
  </label>
</template>

<script setup>
  defineProps({
    modelValue: {
      type: Boolean,
      default: false,
    },
  });

  defineEmits(['update:modelValue'])

</script>

<style scoped>
  ::selection {
    display: none;
  }
</style>
